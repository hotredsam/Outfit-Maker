export const CREATE_ACCOUNT_SUCCESS = 'CREATE_ACCOUNT_SUCCESS';

export const createAccountSuccess = account => ({
    type: CREATE_ACCOUNT_SUCCESS,
    payload: account,
});

export const createAccount = accountData => dispatch => {
    // You can send a POST request to your server here to create the account

    // After successful account creation, dispatch the action
    const newAccount = { ...accountData, id: Date.now() };
    dispatch(createAccountSuccess(newAccount));
};